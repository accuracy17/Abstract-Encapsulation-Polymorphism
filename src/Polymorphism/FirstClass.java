package Polymorphism;

public class FirstClass {
	
	public void book() {
		
	}
	public int book(int a) {
		int sum = a;
		return sum;
	}
	public int book(int a, int b) {
		int sum = a + b;
		return sum;
	}
	public int book(int a, int b, int c) {
		int sum = a + b + c;
		return sum;
	}

}
