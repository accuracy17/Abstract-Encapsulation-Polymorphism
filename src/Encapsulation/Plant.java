package Encapsulation;

public class Plant {
	private String names;
	private int health;
	private int stems;
	
	public String getNames() {
		return names;
	}
	
	public void setNames(String names) {
		this.names = names;
	}
	public int getHealth() {
		return health;
	}
	
	public void setHealth(int health) {
		this.health = health;
	}
	
	public int getStems() {
		return stems;
	}
	
	public void setStems(int stems) {
		this.stems = stems;
	}
	

}
